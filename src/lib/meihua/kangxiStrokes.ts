/**
 * 获取单个汉字的康熙笔画数
 * 优先查字典，未命中时使用兜底估算
 */
export function getStrokeCount(char: string): { strokes: number; estimated: boolean } {
  const strokes = KANGXI_STROKES[char];
  if (strokes !== undefined) {
    return { strokes, estimated: false };
  }
  // 兜底：基于 Unicode 编码的简单估算，保证返回 1-20 之间的值
  const code = char.charCodeAt(0);
  const fallback = (code % 17) + 4; // 4-20 画
  return { strokes: fallback, estimated: true };
}
/**
 * 计算一组汉字的总笔画数
 */
export function getTotalStrokes(text: string): {
  total: number;
  details: { char: string; strokes: number; estimated: boolean }[];
} {
  const details = Array.from(text).map((char) => {
    const { strokes, estimated } = getStrokeCount(char);
    return { char, strokes, estimated };
  });
  const total = details.reduce((sum, d) => sum + d.strokes, 0);
  return { total, details };
}
