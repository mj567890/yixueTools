/* ===== 运行时数据结构 ===== */

/** 主字库（按字索引） */
let _charMap: Map<string, CharEntry> | null = null;
/** 五行索引 */
let _wuxingIndex: Map<WuXing, CharEntry[]> | null = null;
/** 笔画索引 */
let _strokesIndex: Map<number, CharEntry[]> | null = null;

/** 初始化字库（懒加载，首次调用时执行） */
function initDatabase(): void {
  if (_charMap) return;

  _charMap = new Map();
  _wuxingIndex = new Map([
    ['木', []], ['火', []], ['土', []], ['金', []], ['水', []],
  ]);
  _strokesIndex = new Map();

  // 解析紧凑数据
  for (const raw of RAW_DATA) {
    const entry = parseRaw(raw);
    // 如果同一个字出现多次，取第一个（或优先取名常用度更高的）
    if (!_charMap.has(entry.char)) {
      _charMap.set(entry.char, entry);
      _wuxingIndex.get(entry.wuxing)!.push(entry);

      const strokeArr = _strokesIndex.get(entry.kangxiStrokes);
      if (strokeArr) {
        strokeArr.push(entry);
      } else {
        _strokesIndex.set(entry.kangxiStrokes, [entry]);
      }
    }
  }
}

/* ===== 公开 API ===== */

/** 获取单字信息（优先字库，兜底康熙笔画字典） */
export function getCharEntry(char: string): CharEntry | null {
  initDatabase();
  const entry = _charMap!.get(char);
  if (entry) return entry;

  // 兜底：如果不在命名字库但在康熙笔画字典中，生成基础条目
  const strokes = KANGXI_STROKES[char];
  if (strokes !== undefined) {
    // 根据部首推断五行（简化逻辑）
    const wuxing = guessWuxing(char);
    const fallback: CharEntry = {
      char,
      kangxiStrokes: strokes,
      wuxing,
      pinyin: char,
      tone: 1,
      pingZe: '平',
      radical: '',
      meaning: '',
      nameFrequency: 'moderate',
      genderHint: 'neutral',
    };
    return fallback;
  }

  return null;
}

/** 获取字的康熙笔画（优先字库，兜底康熙字典） */
export function getKangxiStrokes(char: string): number {
  initDatabase();
  const entry = _charMap!.get(char);
  if (entry) return entry.kangxiStrokes;
  return KANGXI_STROKES[char] ?? 0;
}

/** 按五行查询字库 */
export function getCharsByWuxing(wx: WuXing): CharEntry[] {
  initDatabase();
  return _wuxingIndex!.get(wx) ?? [];
}

/** 按笔画查询字库 */
export function getCharsByStrokes(strokes: number): CharEntry[] {
  initDatabase();
  return _strokesIndex!.get(strokes) ?? [];
}

/** 按五行+笔画组合查询 */
export function getCharsByWuxingAndStrokes(wx: WuXing, strokes: number): CharEntry[] {
  initDatabase();
  const wxChars = _wuxingIndex!.get(wx) ?? [];
  return wxChars.filter(c => c.kangxiStrokes === strokes);
}

/** 按性别倾向过滤 */
export function filterByGender(chars: CharEntry[], gender: 'male' | 'female'): CharEntry[] {
  return chars.filter(c => c.genderHint === gender || c.genderHint === 'neutral');
}

/** 排除指定字 */
export function excludeChars(chars: CharEntry[], avoidList: string[]): CharEntry[] {
  const avoidSet = new Set(avoidList);
  return chars.filter(c => !avoidSet.has(c.char));
}

/** 获取字库统计信息 */
export function getDatabaseStats(): { total: number; byWuxing: Record<string, number>; strokeRange: [number, number] } {
  initDatabase();
  const total = _charMap!.size;
  const byWuxing: Record<string, number> = {};
  for (const [wx, arr] of _wuxingIndex!) {
    byWuxing[wx] = arr.length;
  }
  let minStroke = Infinity;
  let maxStroke = 0;
  for (const strokes of _strokesIndex!.keys()) {
    if (strokes < minStroke) minStroke = strokes;
    if (strokes > maxStroke) maxStroke = strokes;
  }
  return { total, byWuxing, strokeRange: [minStroke, maxStroke] };
}

/* ===== 内部辅助 ===== */

/** 简单根据字推断五行（基于偏旁部首或字形特征） */
function guessWuxing(char: string): WuXing {
  // 尝试常见偏旁匹配
  for (const [radical, wx] of Object.entries(RADICAL_WUXING)) {
    // 检查是否包含偏旁字符（简化判断）
    if (char.includes(radical)) {
      return wx;
    }
  }
  // 默认返回土（中性）
  return '土';
}
