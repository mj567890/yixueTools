const GENDER_MAP: Array<'neutral' | 'male' | 'female'> = ['neutral', 'male', 'female'];
const FREQ_MAP: Array<'common' | 'moderate' | 'rare'> = ['common', 'moderate', 'rare'];

/** 解析单条紧凑数据为 CharEntry */
function parseRaw(raw: RawChar): CharEntry {
  const tone = raw[4] as 1 | 2 | 3 | 4;
  return {
    char: raw[0],
    kangxiStrokes: raw[1],
    wuxing: WX_MAP[raw[2]],
    pinyin: raw[3],
    tone,
    pingZe: (tone === 1 || tone === 2 ? '平' : '仄') as PingZe,
    radical: raw[5],
    meaning: raw[6],
    nameFrequency: FREQ_MAP[raw[8]],
    genderHint: GENDER_MAP[raw[7]],
  };
}

